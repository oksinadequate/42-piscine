#include <unistd.h>

void	ft_putnbr(int nb)
{
	long int ch;

	if (nb < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-(nb / 10));
		ch = 48 - nb % 10;
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ch = nb % 10 + 48;
	}
	else
	{
		ch = nb + 48;
	}
	write(1, &ch, 1);
}
